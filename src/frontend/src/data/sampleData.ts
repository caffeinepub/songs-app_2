function parseDuration(str: string): number {
  const [m, s] = str.split(":").map(Number);
  return m * 60 + s;
}

const makeSong = (
  id: string,
  title: string,
  artistId: string,
  artistName: string,
  year: number,
  duration: string,
  genre: string,
  gradientClass: string,
): Song => ({
  id,
  title,
  artistId,
  artistName,
  year,
  duration,
  durationSeconds: parseDuration(duration),
  genre,
  gradientClass,
});

